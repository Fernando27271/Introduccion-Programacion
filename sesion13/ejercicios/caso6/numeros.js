function enteros() {
    for(i=1;i<=20;i++)
    {
        document.write(i+"<br>")
    }
}
function pares() {
    for(i=1;i<=20;i++)
    {
        if (i%2==0) {
            document.write(i+"<br>")   
        }
    }
}
function impares() {
    for(i=1;i<=20;i++)
    {
        if (i%2!=0) {
            document.write(i+"<br>")   
        }
    }
}